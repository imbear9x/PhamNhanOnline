    private void GridOnCellFormatting(object? sender, DataGridViewCellFormattingEventArgs e)
    {
        if (_isReloadingData || e.RowIndex < 0 || e.ColumnIndex < 0)
            return;

        if (_grid.Rows[e.RowIndex].DataBoundItem is not DataRowView rowView)
            return;

        var columnName = _grid.Columns[e.ColumnIndex].DataPropertyName;
        if (string.IsNullOrWhiteSpace(columnName))
            return;

        var rowTable = rowView.Row.Table;
        if (rowTable is null)
            return;

        if (!rowTable.Columns.Contains(columnName))
            return;

        if (rowTable.Columns[columnName] is not DataColumn column)
            return;

        if (TryGetValidationMessage(rowView.Row, column, out var message))
        {
            e.CellStyle ??= new DataGridViewCellStyle();
            e.CellStyle.BackColor = Color.MistyRose;
            e.CellStyle.SelectionBackColor = Color.IndianRed;
            e.CellStyle.SelectionForeColor = Color.White;
        }
    }

    private bool TryGetValidationMessage(DataRow row, DataColumn column, out string message)
    {
        message = string.Empty;
        if (row.RowState == DataRowState.Deleted)
            return false;

        if (IsMissingRequiredValue(row, column))
        {
            message = $"Cot '{column.ColumnName}' la bat buoc.";
            return true;
        }

        if (IsDuplicateValue(row, column))
        {
            message = $"Gia tri cua cot '{column.ColumnName}' dang bi trung theo rang buoc unique.";
            return true;
        }

        return false;
    }

    private bool IsDuplicateValue(DataRow row, DataColumn column)
    {
        var rowTable = row.Table;
        if (rowTable is null)
            return false;

        foreach (Constraint constraint in rowTable.Constraints)
        {
            if (constraint is not UniqueConstraint uniqueConstraint)
                continue;

            if (!uniqueConstraint.Columns.Any(x => x.ColumnName == column.ColumnName))
                continue;

            if (uniqueConstraint.Columns.Any(x => IsNullLike(row[x])))
                continue;

            var duplicates = rowTable.Rows
                .Cast<DataRow>()
                .Where(other => other.RowState != DataRowState.Deleted && !ReferenceEquals(other, row))
                .Where(other => uniqueConstraint.Columns.All(x => Equals(other[x], row[x])))
                .Any();

            if (duplicates)
                return true;
        }

        return false;
    }

    private void TryUpdateCodeFromName(DataRow row)
    {
        if (_table is null || !_table.Columns.Contains("name") || !_table.Columns.Contains("code"))
            return;

        var currentCode = Convert.ToString(row["code"]);
        if (!_autoGeneratedCodeRows.Contains(row) &&
            !string.IsNullOrWhiteSpace(currentCode))
        {
            return;
        }

        var name = Convert.ToString(row["name"]);
        if (string.IsNullOrWhiteSpace(name))
            return;

        var generatedCode = GenerateUniqueCodeFromName(name, row);
        row["code"] = generatedCode;
        _autoGeneratedCodeRows.Add(row);
    }

    private string GenerateUniqueCodeFromName(string name, DataRow currentRow)
    {
        var normalized = RemoveDiacritics(name.Trim()).ToLowerInvariant();
        var builder = new StringBuilder(normalized.Length);
        var previousWasSeparator = false;
        foreach (var character in normalized)
        {
            if (char.IsLetterOrDigit(character))
            {
                builder.Append(character);
                previousWasSeparator = false;
            }
            else if (!previousWasSeparator)
            {
                builder.Append('_');
                previousWasSeparator = true;
            }
        }

        var baseCode = builder.ToString().Trim('_');
        if (string.IsNullOrWhiteSpace(baseCode))
            baseCode = "new_code";

        if (_table is null)
            return baseCode;

        var existingCodes = _table.Rows
            .Cast<DataRow>()
            .Where(row => row.RowState != DataRowState.Deleted && !ReferenceEquals(row, currentRow))
            .Select(row => Convert.ToString(row["code"]))
            .Where(code => !string.IsNullOrWhiteSpace(code))
            .ToHashSet(StringComparer.OrdinalIgnoreCase);

        if (!existingCodes.Contains(baseCode))
            return baseCode;

        var index = 1;
        while (existingCodes.Contains($"{baseCode}_{index}"))
            index++;

        return $"{baseCode}_{index}";
    }

    private static string RemoveDiacritics(string text)
    {
        var normalized = text.Normalize(NormalizationForm.FormD);
        var builder = new StringBuilder(normalized.Length);
        foreach (var character in normalized)
        {
            var unicodeCategory = CharUnicodeInfo.GetUnicodeCategory(character);
            if (unicodeCategory != UnicodeCategory.NonSpacingMark)
                builder.Append(character);
        }

        return builder.ToString().Normalize(NormalizationForm.FormC);
    }

    private void BuildGridColumns(AdminTableLoadRequest request, DataTable table)
    {
        _grid.AutoGenerateColumns = false;
        _grid.Columns.Clear();

        foreach (DataColumn column in table.Columns)
        {
            var binding = AdminColumnBindingCatalog.Find(request.Resource.TableName, column.ColumnName);
            DataGridViewColumn gridColumn;
            if (_lookupOptionsByColumn.TryGetValue(column.ColumnName, out var lookupOptions))
            {
                gridColumn = new DataGridViewComboBoxColumn
                {
                    DataPropertyName = column.ColumnName,
                    DataSource = lookupOptions.ToList(),
                    ValueMember = nameof(LookupOption.Value),
                    DisplayMember = nameof(LookupOption.Display),
                    DisplayStyle = DataGridViewComboBoxDisplayStyle.DropDownButton,
                    FlatStyle = FlatStyle.Popup
                };
            }
            else if (column.DataType == typeof(bool))
            {
                gridColumn = new DataGridViewCheckBoxColumn
                {
                    DataPropertyName = column.ColumnName
                };
            }
            else
            {
                gridColumn = new DataGridViewTextBoxColumn
                {
                    DataPropertyName = column.ColumnName
                };
            }

            gridColumn.Name = column.ColumnName;
            gridColumn.HeaderText = binding?.HeaderText ?? AdminColumnBindingCatalog.ToHeaderText(column.ColumnName);
            gridColumn.HeaderCell.ToolTipText = AdminFieldHelpCatalog.BuildHelpText(request.Resource, column, binding);
            gridColumn.ReadOnly = column.ReadOnly;
            _grid.Columns.Add(gridColumn);
        }
    }

    private void SelectFirstRow()
    {
        if (_grid.Rows.Count == 0 || _grid.Columns.Count == 0)
        {
            ClearGridSelection();
            return;
        }

        var firstVisibleRow = _grid.Rows
            .Cast<DataGridViewRow>()
            .FirstOrDefault(row => row.Visible && !row.IsNewRow && row.Cells.Count > 0);

        if (firstVisibleRow is null)
        {
            ClearGridSelection();
            return;
        }

        _grid.ClearSelection();
        firstVisibleRow.Selected = true;
        _grid.CurrentCell = firstVisibleRow.Cells[0];
    }

    private void SelectRow(DataRow row)
    {
        foreach (DataGridViewRow gridRow in _grid.Rows)
        {
            if (gridRow.DataBoundItem is not DataRowView rowView || !ReferenceEquals(rowView.Row, row))
                continue;

            _grid.ClearSelection();
            gridRow.Selected = true;
            if (gridRow.Cells.Count > 0)
                _grid.CurrentCell = gridRow.Cells[0];
            TryScrollRowIntoView(gridRow);
            return;
        }
    }

    private void ApplyFilter()
    {
        if (_table?.DefaultView is null)
            return;

        var keyword = _filterTextBox.Text.Trim().Replace("'", "''");
        if (string.IsNullOrWhiteSpace(keyword))
        {
            _table.DefaultView.RowFilter = string.Empty;
            EnsureGridSelectionMatchesView();
            _statusLabel.Text = _request is null
                ? "San sang."
                : $"Dang hien {_table.DefaultView.Count}/{_table.Rows.Count} dong cua {_request.Resource.TableName}.";
            return;
        }

        var filterParts = _table.Columns
            .Cast<DataColumn>()
            .Where(CanFilterColumn)
            .Select(column => $"Convert([{column.ColumnName}], 'System.String') LIKE '%{keyword}%'")
            .ToArray();

        if (filterParts.Length == 0)
        {
            _statusLabel.Text = "Bang nay khong co cot phu hop de loc nhanh.";
            return;
        }

        try
        {
            _table.DefaultView.RowFilter = string.Join(" OR ", filterParts);
            EnsureGridSelectionMatchesView();
            _statusLabel.Text = _request is null
                ? $"Dang hien {_table.DefaultView.Count}/{_table.Rows.Count} dong."
                : $"Dang hien {_table.DefaultView.Count}/{_table.Rows.Count} dong cua {_request.Resource.TableName}.";
        }
        catch (EvaluateException)
        {
            _table.DefaultView.RowFilter = string.Empty;
            EnsureGridSelectionMatchesView();
            _statusLabel.Text = "Khong the ap dung bo loc cho bang nay.";
        }
    }

    private void EnsureGridSelectionMatchesView()
    {
        if (_grid.Rows.Count == 0 || _grid.Columns.Count == 0)
        {
            ClearGridSelection();
            return;
        }

        if (_grid.CurrentRow is { Visible: true, IsNewRow: false } currentRow &&
            currentRow.DataBoundItem is DataRowView currentView &&
            ReferenceEquals(currentView.Row.Table, _table))
        {
            return;
        }

        SelectFirstRow();
    }

    private void ClearGridSelection()
    {
        _grid.ClearSelection();
        _grid.CurrentCell = null;
    }

    private void TryScrollRowIntoView(DataGridViewRow row)
    {
        if (row.IsNewRow || !row.Visible || row.Index < 0 || _grid.RowCount == 0)
            return;

        try
        {
            _grid.FirstDisplayedScrollingRowIndex = row.Index;
        }
        catch (InvalidOperationException)
        {
            // WinForms can throw when the grid is too small or not fully laid out yet.
            // Selection still succeeded, so we can safely ignore the scroll failure.
        }
    }

    private static bool CanFilterColumn(DataColumn column)
    {
        var type = column.DataType;
        return type == typeof(string)
            || type == typeof(short)
            || type == typeof(int)
            || type == typeof(long)
            || type == typeof(decimal)
            || type == typeof(double)
            || type == typeof(float)
            || type == typeof(bool)
            || type == typeof(DateTime)
            || type == typeof(Guid);
    }

    private static bool TryParseFlexibleNumber(string text, Type targetType, out object parsedValue)
    {
        parsedValue = default!;

        var normalized = text.Replace(" ", string.Empty);
        var styles = NumberStyles.AllowLeadingSign | NumberStyles.AllowDecimalPoint | NumberStyles.AllowThousands;
        var cultures = new[]
        {
            CultureInfo.CurrentCulture,
            CultureInfo.InvariantCulture,
            CultureInfo.GetCultureInfo("vi-VN"),
            CultureInfo.GetCultureInfo("en-US")
        };

        foreach (var culture in cultures.DistinctBy(static x => x.Name))
        {
            if (targetType == typeof(decimal) && decimal.TryParse(normalized, styles, culture, out var decimalValue))
            {
                parsedValue = decimalValue;
                return true;
            }

            if (targetType == typeof(double) && double.TryParse(normalized, styles, culture, out var doubleValue))
            {
                parsedValue = doubleValue;
                return true;
            }

            if (targetType == typeof(float) && float.TryParse(normalized, styles, culture, out var floatValue))
            {
                parsedValue = floatValue;
                return true;
            }
        }

        var alternate = normalized.Contains(',')
            ? normalized.Replace(",", ".")
            : normalized.Replace(".", ",");

        foreach (var culture in cultures.DistinctBy(static x => x.Name))
        {
            if (targetType == typeof(decimal) && decimal.TryParse(alternate, styles, culture, out var decimalValue))
            {
                parsedValue = decimalValue;
                return true;
            }

            if (targetType == typeof(double) && double.TryParse(alternate, styles, culture, out var doubleValue))
            {
                parsedValue = doubleValue;
                return true;
            }

            if (targetType == typeof(float) && float.TryParse(alternate, styles, culture, out var floatValue))
            {
                parsedValue = floatValue;
                return true;
            }
        }

        return false;
    }


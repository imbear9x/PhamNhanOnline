    int? FortuneUpgradeCount,
    int? ActiveMartialArtId,
    bool? PotentialRewardLocked,
    IReadOnlyList<PotentialUpgradePreviewDto>? PotentialUpgradePreviews)
{
    public static CharacterBaseStatsDto FromEntity(CharacterBaseStat entity, int? realmLifespan = null) =>
        new(
            entity.CharacterId,
            entity.RealmId,
            realmLifespan,
            entity.Cultivation,
            entity.BaseHp,
            entity.BaseMp,
            entity.BaseAttack,
            entity.BaseSpeed,
            entity.BaseSpiritualSense,
            entity.BaseStamina,
            entity.LifespanBonus,
            entity.BaseFortune,
            entity.BasePotential,
            entity.UnallocatedPotential,
            entity.CultivationProgress,
            entity.BaseHp,
            entity.BaseMp,
            entity.BaseAttack,
            entity.BaseSpeed,
            entity.BaseSpiritualSense,
            entity.BaseStamina,
            entity.BaseFortune,
            entity.HpUpgradeCount,
            entity.MpUpgradeCount,
            entity.AttackUpgradeCount,
            entity.SpeedUpgradeCount,
            entity.SpiritualSenseUpgradeCount,
            entity.FortuneUpgradeCount,
            entity.ActiveMartialArtId,
            entity.PotentialRewardLocked,
            null);
}

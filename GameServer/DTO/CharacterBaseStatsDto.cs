    int? FinalHp,
    int? FinalMp,
    int? FinalAttack,
    int? FinalSpeed,
    int? FinalSpiritualSense,
    int? FinalStamina,
    double? FinalFortune,
    int? HpUpgradeCount,
    int? MpUpgradeCount,
    int? AttackUpgradeCount,
    int? SpeedUpgradeCount,

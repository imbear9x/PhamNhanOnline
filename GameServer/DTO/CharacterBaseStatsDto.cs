    Guid CharacterId,
    int? RealmTemplateId,
    int? RealmLifespan,
    long? Cultivation,
    int? BaseHp,
    int? BaseMp,
    int? BaseAttack,
    int? BaseSpeed,
    int? BaseSpiritualSense,
    int? BaseStamina,
    int? LifespanBonus,
    double? BaseFortune,
    int? BasePotential,
    int? UnallocatedPotential,
